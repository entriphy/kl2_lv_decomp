void hKlonoaDead(s32 vision, s32 frame, DEAD pattern) {
    gD->deadFlag = 1;
    gD->deadCnt = frame;

    switch (pattern) {
        case DEAD_DAMAGE:
            hSeKeyOn(0x4b04b01ca1000b, NULL, 0);
            hSeKeyOn(0xc80c80ca20109, NULL, 0);
            break;
        case DEAD_FALL:
            hSeKeyOn(0x4b04b01ca1000c, NULL, 0);
            hSeKeyOn(0xc80c80ca20108, NULL, 0);
            break;
        case DEAD_DROWN:
            hSeKeyOn(0xc80c80ca10008, NULL, 0);
            hSeKeyOn(0x4b04b00ca1000e, NULL, 0);
            hSeKeyOn(0xc80c80ca2010a, NULL, 0);
            break;
        case DEAD_CHOKE:
        case DEAD_BURN:
            break;
    }

    if (hGameBgmComp(GameGbl.vision, gD->BGMid, gD->resVision, gD->resBGM) != 0 || vision == -1)
    hBgmFadeOut(60);
}

